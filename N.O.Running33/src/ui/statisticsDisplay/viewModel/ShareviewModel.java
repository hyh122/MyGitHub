package ui.statisticsDisplay.viewModel;


public class ShareviewModel {
	private String date;
	private String step;
	private String calouis;
	private String usetime;
	private String avgheartrate;
	private String avgspeed;
	
	public String getUsetime() {
		return usetime;
	}
	public void setUsetime(String usetime) {
		this.usetime = usetime;
	}
	public String getAvgheartrate() {
		return avgheartrate;
	}
	public void setAvgheartrate(String avgheartrate) {
		this.avgheartrate = avgheartrate;
	}
	public String getAvgspeed() {
		return avgspeed;
	}
	public void setAvgspeed(String avgspeed) {
		this.avgspeed = avgspeed;
	}
	
	
	
	
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getStep() {
		return step;
	}
	public void setStep(String step) {
		this.step = step;
	}
	public String getCalouis() {
		return calouis;
	}
	public void setCalouis(String calouis) {
		this.calouis = calouis;
	}
	
}
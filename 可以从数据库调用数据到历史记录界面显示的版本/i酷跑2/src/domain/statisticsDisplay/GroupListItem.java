package domain.statisticsDisplay;

public class GroupListItem {
	private String date;
	
	
	public GroupListItem() {
		super();
	}

	public GroupListItem(String date) {
		super();
		this.date = date;
	}

	public String getDate() {
		return date;
	}

	public void setDate(String date) {
		this.date = date;
	}
	
}
